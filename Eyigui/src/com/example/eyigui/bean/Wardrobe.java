package com.example.eyigui.bean;


public class Wardrobe {
	private String wardrobeID;
	private int clothesHoldNumber;
	private String userID;

	public Wardrobe() {
	}

	public String getWardrobeID() {
		return wardrobeID;
	}

	public void setWardrobeID(String wardrobeID) {
		this.wardrobeID = wardrobeID;
	}

	public int getClothesHoldNumber() {
		return clothesHoldNumber;
	}

	public void setClothesHoldNumber(int clothesHoldNumber) {
		this.clothesHoldNumber = clothesHoldNumber;
	}

	public String getUserID() {
		return userID;
	}

	public void setUserID(String userID) {
		this.userID = userID;
	}

}
