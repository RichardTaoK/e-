package com.example.eyigui.bean;

import java.util.Date;

public class Clothes {
	private String clothesID;
	private Date clothesDate;
	private String clothesType;
	private String clothesUrl;
	private String clothesDescription;
	private String clothesSeason;
	private String clothesColor;

	public Clothes() {
	}

	public String getClothesID() {
		return clothesID;
	}

	public void setClothesID(String clothesID) {
		this.clothesID = clothesID;
	}

	public Date getClothesDate() {
		return clothesDate;
	}

	public void setClothesDate(Date clothesDate) {
		this.clothesDate = clothesDate;
	}

	public String getClothesType() {
		return clothesType;
	}

	public void setClothesType(String clothesType) {
		this.clothesType = clothesType;
	}

	public String getClothesUrl() {
		return clothesUrl;
	}

	public void setClothesUrl(String clothesUrl) {
		this.clothesUrl = clothesUrl;
	}

	public String getClothesDescription() {
		return clothesDescription;
	}

	public void setClothesDescription(String clothesDescription) {
		this.clothesDescription = clothesDescription;
	}

	public String getClothesSeason() {
		return clothesSeason;
	}

	public void setClothesSeason(String clothesSeason) {
		this.clothesSeason = clothesSeason;
	}

	public String getClothesColor() {
		return clothesColor;
	}

	public void setClothesColor(String clothesColor) {
		this.clothesColor = clothesColor;
	}

}
